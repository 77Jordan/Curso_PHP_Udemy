<?php 

// += 
$a = 0;
$a += 10; // $a = $a + 10

echo $a . "<br>";

// -= 
$a -= 10; // $a = $a - 10

echo $a . "<br>";

// *= 
$a = 10;
$a *= 10; // $a = $a * 10

echo $a . "<br>";

// /= 
$a = 100;
$a /= 10; // $a = $a / 10

echo $a . "<br>";

// %= 
$a = 99;
$a %= 10; // $a = $a % 10

echo $a . "<br>";

// UTILIZAÇÃO COM VARIÁVEIS
$f = 10;
$x = 20;

$f += $x; // $f = $f + $x
echo $f . "<br>";
?>